    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String sen="Sencilla";
        String dob="Doble";
        String tri="Triple";
        String ocu="Ocupada";
        sencilla=0;doble=0;triple=0;
        this.conn=new MySqlConn();
        String query ="select * from habitaciones";
        this.conn.Consult(query);
        int n=0;
        try{
            this.conn.rs.last();
            n=this.conn.rs.getRow();
            this.conn.rs.first();            
        }catch(Exception e){
            System.out.println("Error #1...");
        }
        if(n!=0){
            String tipo,disponibilidad ;
            for (int i = 0; i < n; i++) {
                try{
                    disponibilidad=this.conn.rs.getString(5);
                    tipo=this.conn.rs.getString(1);
                  if(tipo.equals(sen)){
                      if(disponibilidad.equals(ocu)){
                          sencilla=sencilla+1;
                      }
                  }
                   if(tipo.equals(dob)){
                      if(disponibilidad.equals(ocu)){
                          doble=doble+1;
                      }
                  }
                   if(tipo.equals(tri)){
                      if(disponibilidad.equals(ocu)){
                          triple=triple+1;
                      }
                  }
                  
                    
                    this.conn.rs.next();
                }catch(Exception e){
                    System.out.println("Error #2");
                }
            }
        }
        repaint();
    }//GEN-LAST:event_jButton1ActionPerformed

