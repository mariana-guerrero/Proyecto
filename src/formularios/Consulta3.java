    public void paint(Graphics g){
            super.paint(g);
            int sencillaa = sencilla;
            int doblee = doble;
            int triplee = triple;
            
            int total = sencillaa+doblee+triplee;
            int grados_sencilla = sencillaa * 360 / total;
            int grados_doble = doblee * 360 / total;
            int grados_triple = triplee * 360 / total;
            
            g.setColor(new Color(255, 0, 0));
            g.fillArc(25, 80, 200, 200, 0, grados_sencilla);
            g.fillRect(250, 120, 20, 20);
            g.drawString("Sencilla= "+sencilla, 275, 135);
            
            g.setColor(new Color(0, 130, 0));
            g.fillArc(25, 80, 200, 200, grados_sencilla, grados_doble);
            g.fillRect(250, 150, 20, 20);
            g.drawString("Doble= "+doble, 275, 165);
            
            g.setColor(new Color(0, 0, 255));
            g.fillArc(25, 80, 200, 200, grados_sencilla + grados_doble, grados_triple);
            g.fillRect(250, 180, 20, 20);
            g.drawString("Triple= "+triple, 275, 195);                   
    }
